class Sample{
 public static void main(String args[])
{
int a = 10;
int b = 5;
System.out.println("a+b="+(a+b));
System.out.println("a-b="+(a-b));
}
}


class UnaryOperatorDemo{
 public static void main(String args[])
{
int a = 10;
int b = 15;
int c = ++a;
int d = b++;
System.out.println("c:"+c);
System.out.println("d:"+d);
System.out.println("b:"+b);
}
}


